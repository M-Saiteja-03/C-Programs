#include<stdio.h>
int main()
{
   int a=10,b=10,c=10,d=10,e=10;
   printf("SHORTAND ARITHMATIC AND ASSIGNMENT OPERATORS\n a=10,b=10,c=10,d=10,e=10 \n");
   a+=5;
   printf("a+=5 a=%d \n",a);
   b-=5;
   printf("b-=5 b=%d \n",b);
   c*=5;
   printf("c*=5 c=%d \n",c);
   d/=5;
   printf("d/=5 d=%d \n",d);
   e%=5;
   printf("e%=5 e=%d \n",e);


   return 0;
}
